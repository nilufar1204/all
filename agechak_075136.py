while True:
    i = int(input('দয়া করে আপনার বয়স লিখুন : '))

    if i <= 13:
        print('আপনি এক জন শিশু')
    elif i <= 18:
        print('আপনি এক জন কিশোর')
    else:
        print('আপনি এক জন যুবক')

    repeat = input('আপনি কি আবার চালিয়ে যেতে চান? (হ্যাঁ/না): ')
    if repeat.lower() == 'না':
        break  # শুধুমাত্র "না" দিলে প্রোগ্রাম বন্ধ হবে

print('ধন্যবাদ! আবার আসবেন ')